/**
 * Looks up the NEM score for a given GPA and school type.
 * Returns null if GPA is out of the 4.00–7.00 range.
 */
export function lookupNem(
  promedio: number,
  schoolType: SchoolType
): number | null {
  if (promedio < 4.0 || promedio > 7.0) return null;

  const table = TABLE_MAP[schoolType];
  const index = Math.round((promedio - 4.0) * 100);
  const clamped = Math.max(0, Math.min(300, index));
  return table[clamped];
}
